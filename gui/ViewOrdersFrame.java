    private void jcComboOrdersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcComboOrdersActionPerformed
         String orderId = jcComboOrders.getSelectedItem().toString();   
         tm = (DefaultTableModel) jtShow.getModel();
         
         tm.setRowCount(0);
          try {
            List<ProductsPojo> prods;
            if (UserProfile.getUsertype().equals(UserDAO.RECEPTIONIST)) {
                prods = OrdersDAO.getOrders(orderId);
            } else {
                prods = OrdersDAO.getAllOrders(orderId);
            }
            Object[] rows = new Object[8];
            for (ProductsPojo p : prods) {

                rows[0] = p.getProductId();
                rows[1] = p.getProductName();
                rows[2] = p.getProductCompany();
                rows[3] = p.getProductPrice();
                rows[4] = p.getOurPrice();
                rows[5] = p.getQuantity();
                rows[6] = p.getTax() + "%";
                rows[7] = (p.getOurPrice() + (p.getTax() * p.getOurPrice() / 100.0)) * p.getQuantity();

                tm.addRow(rows);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcComboOrdersActionPerformed

