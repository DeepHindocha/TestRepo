    private void loadOrderId() {
         try {
            List<String> ids;
            if (UserProfile.getUsertype().equals(UserDAO.RECEPTIONIST)) {
                 ids = OrdersDAO.getAllOrderIds(UserProfile.getUserid());
            } else {
                ids = OrdersDAO.getAllOrderIds(null);
            }
            if (ids.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No Orders Found!");
                return;
            }
            ids.forEach(id -> {
                jcComboOrders.addItem(id);
            });
        } catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(null, "DB Error");
            ex.printStackTrace();
        }
    }
